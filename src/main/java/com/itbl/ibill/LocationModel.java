package com.itbl.ibill;

public class LocationModel {

	String name, code;

	public LocationModel(String name, String code) {
		super();
		this.name = name;
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	@Override
	public String toString() {
		return "LocationModel [name=" + name + ", code=" + code + "]";
	}

}
